a = (11, 22, 33)
print(a, type(a)) # tuple
a = [11, 22, 33]
print(a, type(a)) # list
a = {11 : 22, 33 : 44}
print(a, type(a)) # dict
a = {11, 22, 33, 44}
print(a, type(a)) # set


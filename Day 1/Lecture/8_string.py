# string literals
a = "this is a \ntest"
b = 'this is a \ntest'
# no diff bet single and double quotes
c = r'this is a \ntest'
# raw string : escape seq are not expanded
print(a, type(a))
print(b, type(b))
print(c, type(c))
# triple quoted string
d = """
this is a long
sentence
spreading
multiple lines
"""
print(d, type(d))

